require 'json'
require 'date'
load 'scrape_courses.rb'

month = Date::MONTHNAMES[Date.today.month]
if month 

file = File.open("Su 2017 Course Schedule.json", "r")
lines = IO.readlines(file) 
file.close
a_hash = JSON.parse(lines[0].to_s)
iter = 0
a_hash.each do |x|
	if x[1].length > iter then
		iter = x[1].length
	end
end
